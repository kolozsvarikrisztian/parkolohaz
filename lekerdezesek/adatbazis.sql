use parkolohaz;

-- Járműtípusok
CREATE TABLE jarmutipus
(
    id  int         NOT NULL AUTO_INCREMENT,
    nev varchar(32) NOT NULL,
    primary key (id)
);

-- Emelet
CREATE TABLE emelet
(
    id  int         NOT NULL,
    nev varchar(64) NOT NULL,
    primary key (id)
);

-- Felhasználók
CREATE TABLE felhasznalo
(
    id  int         NOT NULL,
    nev varchar(64) NOT NULL,
    primary key (id)
);

-- Parkolóhelyek
CREATE TABLE parkolohely
(
    id           int         NOT NULL,
    jarmutipusId int         NOT NULL,
    emeletId     int         NOT NULL,
    felirat      varchar(32) NOT NULL,
    szelesseg    double      NOT NULL,
    primary key (id),
    key          jarmutipusId (jarmutipusId),
    CONSTRAINT FK_jarmutipus_jarmutipusId FOREIGN KEY (jarmutipusId)
        REFERENCES jarmutipus (id),
    key          emeletId(emeletId),
    CONSTRAINT FK_emelet_emeletId FOREIGN KEY (emeletId)
        REFERENCES emelet (id)
);

-- Foglalások
CREATE TABLE foglalas
(
    id            int  NOT NULL,
    parkolohelyId int  NOT NULL,
    felhasznaloId int  NOT NULL,
    ar            int  NOT NULL,
    datum         date NOT NULL,
    fizetve       boolean default false,
    primary key (id),
    key           parkolohelyId(parkolohelyId),
    CONSTRAINT FK_parkolohely_parkolohelyId FOREIGN KEY (parkolohelyId)
        REFERENCES parkolohely (id),
    key           felhasznaloId(felhasznaloId),
    CONSTRAINT FK_felhasznalo_felhasznaloId FOREIGN KEY (felhasznaloId)
        REFERENCES felhasznalo (id)
);


